#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	c = (char)c;
	i = ft_strlen(s) + 1;
	while (i >= 1)
	{
		if (s[i - 1] == c)
			return ((char *)s + i - 1);
		i--;
	}
	return (0);
}
